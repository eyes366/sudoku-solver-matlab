function [output] = GetNumbersFromGaps(handles)
output = zeros(9,9);
%output(1,1) = str2double(get(handles.edit11,'String'));
for i =1:9
    for j =1:9
        str = ['output(' num2str(i) ',' num2str(j) ') = str2double(get(handles.edit' num2str(i) num2str(j) ',''String''));'];
        eval(str);
    end
end
output(isnan(output)) = 0;

function SetNumbersToGaps(handles, x, s_clue_ori)
for i = 1:9
    for j =1:9
        % if x(1,1) == 0
        % set(handles.edit11,'String','');
        % else set(handles.edit11,'String',x(1,1));
        % end
        str1 = ['if x(' num2str(i) ',' num2str(j) ') == 0'];
        str2 = ['set(handles.edit' num2str(i) num2str(j) ',''String'','''');'];
        str3 = ['else set(handles.edit' num2str(i) num2str(j) ',''String'',x(' num2str(i) ',' num2str(j) '));'];
        str4 = ['end'];
        eval([str1 str2 str3 str4]);
        if s_clue_ori(i,j) ~= 0
            %set(handles.edit11,'ForegroundColor','red')
            str = ['set(handles.edit' num2str(i) num2str(j) ',''ForegroundColor'',''red'')'];
            eval([str]);
        else
            str = ['set(handles.edit' num2str(i) num2str(j) ',''ForegroundColor'',''black'')'];
            eval([str]);
        end
    end
end

function ViewAnswer(handles, n)
