set(handles.text_result,'String',str);
set(handles.text_iterate_deep,'String',['',int2str(g_interate_deep(n))]);
set(handles.text_try_cont,'String',['',int2str(g_try_cont(n))]);
set(handles.text_try_fail,'String',['',int2str(g_try_fail(n)),'/',int2str(g_try_total(n))]);

